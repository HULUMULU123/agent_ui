def validate_op(subject_id: str | None, action_id: str | None, flags: list[str] | None) -> dict[str, Any]:
    """Проверяет subject_id/action_id/flags против закрытого каталога. Несуществующий
    subject_id -> forced unknown; action_id вне allowed_actions -> обнуляется."""
    flags = flags or []
    need_review = False

    if subject_id not in CATALOG_ACTIONS:
        subject_id, action_id = "unknown", None
        need_review = True
    else:
        allowed = CATALOG_ACTIONS[subject_id]
        if action_id is not None and action_id not in allowed:
            action_id = None
            need_review = True

    flags = [f for f in flags if f in ALL_FLAGS]
    return {"subject_id": subject_id, "action_id": action_id, "flags": flags, "need_review": need_review}


def build_operation_row(subject_id: str, action_id: str | None, flags: list[str], purpose: str, direction: str) -> dict[str, Any]:
    """Собирает предварительный (до проверки анализатором) тип + документы -- direction
    здесь уже в конвенции тетрадки ("outflow"/"inflow"), см. _to_catalog_direction."""
    op_type = resolve_operation_type(subject_id, action_id, flags)
    if op_type == "Неопознанный платёж":
        refined = refine_unknown(purpose)
        op_type = refined["operation_type"]
        docs = refined["requested_documents"]
    else:
        docs = resolve_documents(op_type, action_id, flags, direction)
    return {"operation_type": op_type, "requested_documents": docs}


# ---- Детерминированный fallback (без реального LLM) ----

_WORD_CHARS = re.compile(r"\w", re.UNICODE)


def _word_boundary_match(trigger: str, text: str) -> bool:
    """Триггер должен встречаться как отдельное слово/словосочетание, а не как часть
    другого слова -- например, 'поставщик' не должен подтверждать триггер 'поставка' по
    общему корню. Использует lookaround без предположений о конкретных морфемах."""
    pattern = r"(?<!\w)" + re.escape(trigger.lower()) + r"(?!\w)"
    return re.search(pattern, text, re.UNICODE) is not None


# Ключевые слова purpose -> action_id, проверяются в этом порядке (первое совпадение
# побеждает), затем фильтруются по allowed_actions конкретного subject_id. Без совпадения --
# fallback на направление (outflow -> payment, inflow -> receipt), см. шаг 8 промпта классификатора.
_ACTION_KEYWORDS: list[tuple[str, str]] = [
    ("возврат", "return"), ("погашен", "repayment"), ("процент", "interest_payment"),
    ("предостав", "provision"), ("выдач", "provision"), ("уплат", "tax_payment"),
    ("вычет", "tax_refund"), ("приобрет", "purchase"), ("продаж", "sale"),
    ("взнос", "contribution"), ("снят", "withdrawal"),
]


def _infer_action_id(purpose_lower: str, allowed: list[str], direction_catalog: str) -> str | None:
    if not allowed:
        return None
    for keyword, action_id in _ACTION_KEYWORDS:
        if keyword in purpose_lower and action_id in allowed:
            return action_id
    fallback = "receipt" if direction_catalog == "inflow" else "payment"
    if fallback in allowed:
        return fallback
    return allowed[0]


def classify_operation_deterministic(purpose: str, direction: str = "outflow") -> tuple[str, str | None]:
    """Классификатор без LLM: ищет по всем subject_id самый длинный (наиболее специфичный)
    триггер, встречающийся в purpose как подстрока. Длина триггера как tie-breaker отдает
    приоритет более конкретным формулировкам ("договор поставки" длиннее просто "поставка").
    action_id определяется отдельными ключевыми словами (возврат/погашение/проценты и т.д.),
    а не берется автоматически первым из allowed_actions -- иначе "возврат займа" и
    "предоставление займа" (один subject_id "loan") были бы неразличимы."""
    text = (purpose or "").lower()
    best: tuple[int, str] | None = None
    for subject_id, triggers in SUBJECT_TRIGGERS.items():
        if subject_id == "unknown":
            continue
        for trigger in triggers:
            # короткие триггеры (аббревиатуры вроде "ТУ", "СМР") требуют границы слова --
            # иначе они массово ложно совпадают как часть других слов ("ТУ" внутри "поступление").
            matched = _word_boundary_match(trigger, text) if len(trigger) <= 3 else trigger.lower() in text
            if matched:
                if best is None or len(trigger) > best[0]:
                    best = (len(trigger), subject_id)
    if best is None:
        return "unknown", None
    subject_id = best[1]
    allowed = CATALOG_ACTIONS.get(subject_id) or []
    action_id = _infer_action_id(text, allowed, direction)
    return subject_id, action_id


